## Preferred Technologies

### Frontend
- React, Vue.js, Angular
- TypeScript preferred over JavaScript
- Tailwind CSS for styling

### Backend
- Node.js, Python, Java, Go
- RESTful APIs preferred
- GraphQL for complex data requirements

### Databases
- PostgreSQL for relational data
- MongoDB for document storage
- Redis for caching

### Cloud Platforms
- AWS, Azure, Google Cloud
- Docker for containerization
- Kubernetes for orchestration

## Architecture Patterns

### Microservices
- Domain-driven design
- Event-driven architecture
- API-first approach

### Security
- OAuth 2.0 / OpenID Connect
- JWT tokens
- HTTPS everywhere

## Diagram Preferences

### Styling
- Consistent color scheme
- Clear, readable fonts
- Minimal, clean layouts

### Naming
- Business terminology in context diagrams
- Technical terminology in container/component diagrams
- Consistent naming conventions
EOF
fi

# Check for Structurizr Lite dependencies
echo -e "${BLUE}ðŸ” Checking for Structurizr Lite dependencies...${NC}"

# Check for Docker (recommended)
if command -v docker &> /dev/null; then
    echo -e "${GREEN}âœ… Docker found - recommended for Structurizr Lite${NC}"
    echo -e "${BLUE}ðŸ’¡ To start Structurizr Lite with Docker:${NC}"
    echo "   docker pull structurizr/lite"
    echo "   docker run -it --rm -p 8080:8080 -v ~/structurizr:/usr/local/structurizr structurizr/lite"
else
    echo -e "${YELLOW}âš ï¸  Docker not found. You can install Docker or use Java 17+ instead.${NC}"
fi

# Check for Java (alternative)
if command -v java &> /dev/null; then
    JAVA_VERSION=$(java -version 2>&1 | head -n 1 | cut -d'"' -f2 | cut -d'.' -f1)
    if [ "$JAVA_VERSION" -lt 17 ]; then
        echo -e "${YELLOW}âš ï¸  Java version $JAVA_VERSION found. Structurizr Lite Spring Boot requires Java 17+.${NC}"
        echo "Please upgrade to Java 17 or later, or use Docker instead."
        echo "Visit: https://adoptium.net/"
    else
        echo -e "${GREEN}âœ… Java $JAVA_VERSION found - compatible with Structurizr Lite Spring Boot${NC}"
    fi
else
    echo -e "${YELLOW}âš ï¸  Java not found. Install Java 17+ or use Docker instead.${NC}"
    echo "Visit: https://adoptium.net/"
fi

# Create example workspace
echo -e "${BLUE}ðŸ“„ Creating example workspace...${NC}"
cat > ".bmad-c4-architecture/examples/example-workspace.dsl" << EOF
