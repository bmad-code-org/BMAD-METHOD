#!/bin/bash

# BMAD C4 Architecture Expansion Pack Installation Script
# This script installs the C4 Architecture expansion pack into a BMAD-METHOD project

set -e

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# Configuration
EXPANSION_PACK_NAME="bmad-c4-architecture"
TARGET_DIR=".bmad-c4-architecture"
SOURCE_DIR="$(dirname "$0")"

echo -e "${BLUE}ðŸ›ï¸  BMAD C4 Architecture Expansion Pack Installer${NC}"
echo "=================================================="

# Check if we're in a BMAD-METHOD project
if [ ! -f "package.json" ] || ! grep -q "bmad-method" package.json; then
    echo -e "${RED}âŒ Error: This doesn't appear to be a BMAD-METHOD project.${NC}"
    echo "Please run this script from the root of a BMAD-METHOD project."
    exit 1
fi

# Check if expansion pack already exists
if [ -d "$TARGET_DIR" ]; then
    echo -e "${YELLOW}âš ï¸  Expansion pack already exists at $TARGET_DIR${NC}"
    read -p "Do you want to overwrite it? (y/N): " -n 1 -r
    echo
    if [[ ! $REPLY =~ ^[Yy]$ ]]; then
        echo "Installation cancelled."
        exit 0
    fi
    echo -e "${YELLOW}Removing existing expansion pack...${NC}"
    rm -rf "$TARGET_DIR"
fi

# Create target directory
echo -e "${BLUE}ðŸ“ Creating target directory...${NC}"
mkdir -p "$TARGET_DIR"

# Copy expansion pack files
echo -e "${BLUE}ðŸ“‹ Copying expansion pack files...${NC}"
cp -r "$SOURCE_DIR"/* "$TARGET_DIR/"

# Remove the install script from target
rm -f "$TARGET_DIR/install.sh"

# Update core configuration
echo -e "${BLUE}âš™ï¸  Updating core configuration...${NC}"
if [ -f ".bmad-core/core-config.yaml" ]; then
    # Backup existing config
    cp ".bmad-core/core-config.yaml" ".bmad-core/core-config.yaml.backup"
    echo -e "${YELLOW}ðŸ“‹ Backed up existing core-config.yaml${NC}"
fi

# Create or update core config to include C4 architecture
cat > ".bmad-core/core-config.yaml" << EOF
# BMAD Core Configuration
# This file is automatically generated by the C4 Architecture expansion pack

# Developer context files
devLoadAlwaysFiles:
  - docs/architecture/coding-standards.md
  - docs/architecture/tech-stack.md
  - docs/architecture/project-structure.md

# Expansion pack configurations
expansionPacks:
  - name: "c4-architecture"
    enabled: true
    config: ".bmad-c4-architecture/config.yaml"

# C4 Architecture specific settings
c4Architecture:
  structurizrLiteUrl: "http://localhost:8080"
  defaultTheme: "default"
  exportFormats: ["png", "svg", "pdf", "dsl"]
  autoLayout: true
  validationStrict: true
EOF

# Create example technical preferences
if [ ! -f ".bmad-c4-architecture/data/technical-preferences.md" ]; then
    echo -e "${BLUE}ðŸ“ Creating example technical preferences...${NC}"
    cat > ".bmad-c4-architecture/data/technical-preferences.md" << EOF
